def get_completion_from_messages(messages, 
                                 model=MODEL_NAME, 
                                 temperature=0, 
                                 max_tokens=1000):
    response = openai.chat.completions.create(
        model=model,
        messages=messages,
        temperature=temperature, 
        max_tokens=max_tokens, 
    )
    return response.choices[0].message.content


# # Generate a random list of numbers and sort it with GPT

# In[3]:


import random
import ast
import math
import numpy as np
import re

# Optional reproducibility
seed_str = os.getenv("EXPERIMENT_SEED")
if seed_str is not None:
    try:
        seed_val = int(seed_str)
        random.seed(seed_val)
        np.random.seed(seed_val)
    except Exception:
        pass



corrstr = ''
sizestr = ''
maxnum = ''
timestr = ''
sampleRuns = 50

for ubound in [1000, 5000, 10000, 50000, 100000, 500000, 1000000, 5000000, 10000000, 50000000, 100000000, 1000000000, 10000000000]:
    size = 30
    correctness = 0

    for i in range(sampleRuns):
        while True:
            try:
                to_sort =  []
                for j in range(size):
                    to_sort.append(int(10 ** random.uniform(1, math.log(ubound, 10))))

                direction = 'Ascending'



                sys_msg = f"""
                Response should only contain the sorted data in the direction specified by the user.
                Do not say anything else, just return a same structure with the sorted information.
                You role is to only return the data.
                """

                collection_type = f"""
                list
                """

                prompt_nums = f"""
                Sort the elements in the given collection in the {direction} order, and return only the sorted collection in the {collection_type} format:
                {to_sort}
                """



                messages = [
                    {'role': 'system',
                    'content': sys_msg},
                    {'role':'user',
                    'content': prompt_nums}
                ]

                start_time = time.time()

                res = get_completion_from_messages(messages)

                end_time = time.time()
                print(res)

                parsed_ok = False
                try:
                    sorted_list = ast.literal_eval(res)
                    parsed_ok = isinstance(sorted_list, list)
                except Exception:
                    parsed_ok = False
                if not parsed_ok and os.getenv("EVAL_NORMALIZE", "false").lower() in ("1", "true", "yes"):
                    m = re.search(r"\[[\s\S]*\]", res)
                    if m:
                        try:
                            sorted_list = ast.literal_eval(m.group(0))
                            parsed_ok = isinstance(sorted_list, list)
                        except Exception:
                            parsed_ok = False
                if not parsed_ok:
                    raise ValueError("Could not parse list response (strict or normalized)")
                break
            except Exception as ex:
                print("error: ")
                print(ex)



        # # Evaluation

        # In[4]:


        to_sort.sort()

        if(to_sort == sorted_list):
            correctness += 1
            corrstr+="1\n"
        else:
            corrstr+="0\n"
        sizestr += str(size) + '\n'
        timestr += str(end_time - start_time) + '\n'

        maxnum += str(ubound) + '\n'

    print(correctness, sampleRuns, correctness/sampleRuns)

with open(os.path.join(OUTDIR, "correctness10rand.txt"), 'w+') as f:
        f.write(corrstr)
with open(os.path.join(OUTDIR, "sizes10rand.txt"), 'w+') as f:
    f.write(sizestr)
with open(os.path.join(OUTDIR, "maxNum10rand.txt"), 'w+') as f:
    f.write(maxnum)
with open(os.path.join(OUTDIR, "timeSort10rand.txt"), 'w+') as f:
    f.write(timestr)
